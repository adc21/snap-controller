from typing import Dict


def short_path(path: str) -> str:
    """長いパスを短縮して返します。"""
    try:
        parts = path.replace("\\", "/").split("/")
        return "/".join(parts[-3:]) if len(parts) > 3 else path
    except Exception:
        return path


